const checkTxHash = async (publicClient: any, hash: any) => {
  if (hash) {
    const data = await publicClient.waitForTransactionReceipt({
      hash,
    });
    if (data.status == "success") {
      return true;
    }
    return false;
  }
};
//The wallet should has nonce, private key and the token it has.
export class MasterWalletClass {
  publicClient: any;
  WNATIVETOKEN: any;
  router: any;
  constructor() {
    this.router = V2_ROUTER_ADDRESSES[8453];
    this.WNATIVETOKEN = WETH9[8453];
    this.publicClient = createPublicClient({
      chain: base,
      transport: http(),
    });
  }

  //get balance of the wallet
  async GetEthBalance(walletAddress: string) {
    try {
      const balance = await this.publicClient.getBalance({
        address: walletAddress,
      });
      return balance;
    } catch (error: any) {
      throw new Error(`[GetEthBalance]: ${error.message} [${walletAddress}]`);
    }
  }

  async getERC20Balance(walletAddress: string, contractAddress: string) {
    try {
      const balanceOfERC20 = await this.publicClient.readContract({
        address: contractAddress,
        abi: erc20Abi,
        functionName: "balanceOf",
        args: [walletAddress],
      });
      return balanceOfERC20;
    } catch (error: any) {
      throw new Error(`[getERC20Balance]: ${error.message} [${walletAddress}]`);
    }
  }

  async transferETH(walletClient: any, to: string, amount: any) {
    try {
      const hash = await walletClient.sendTransaction({
        to: to,
        value: parseEther(amount),
      });
      if (hash != null) {
        if (await checkTxHash(this.publicClient, hash)) {
          return hash;
        }
        return false;
      }
    } catch (error: any) {
      throw new Error("[Transfer ETH]: " + error.message);
    }
  }

  async getPairAddress(tokenA: any, tokenB: any) {
    return Pair.getAddress(tokenA, tokenB);
  }

  amountIn(inputToken: any, amountIn: any) {
    const typedValueInParsed = parseUnits(
      amountIn.toString(),
      inputToken.decimals
    );
    return typedValueInParsed.toString();
  }
  async createPair(token: any, nativeToken: any) {
    const pairAddress = await this.getPairAddress(token, nativeToken);

    // Setup provider, import necessary ABI ...
    const reserves = await this.publicClient[token.chainId].readContract({
      address: pairAddress,
      abi: uniswapV2poolABI.abi,
      functionName: "getReserves",
      args: [],
    });
    const [reserve0, reserve1] = reserves;

    const tokens = [token, nativeToken];
    const [token0, token1] = tokens[0].sortsBefore(tokens[1])
      ? tokens
      : [tokens[1], tokens[0]];

    const pair = new Pair(
      CurrencyAmount.fromRawAmount(token0, reserve0.toString()),
      CurrencyAmount.fromRawAmount(token1, reserve1.toString())
    );
    return pair;
  }

  async getRoute(
    inputToken: any,
    outputToken: any,
    amountIn: any,
    chainId: any
  ) {
    const pair = await this.createPair(inputToken, outputToken);

    if (inputToken == this.WNATIVETOKEN) {
      const route = new Route([pair], Ether.onChain(chainId), outputToken);
      return new Trade(
        route,
        CurrencyAmount.fromRawAmount(
          Ether.onChain(chainId),
          this.amountIn(inputToken, amountIn)
        ),
        TradeType.EXACT_INPUT
      );
    } else {
      const route = new Route([pair], inputToken, Ether.onChain(chainId));
      return new Trade(
        route,
        CurrencyAmount.fromRawAmount(
          inputToken,
          this.amountIn(inputToken, amountIn)
        ),
        TradeType.EXACT_INPUT
      );
    }
  }

  swapOptions(slippage: any, walletAddress: any, isFeeOnTransfer: any) {
    const userSlippageTolerance = new Percent(slippage * 100, "10000"); //   with slippage 50 ~ 0.5%
    if (!isFeeOnTransfer) {
      return {
        allowedSlippage: userSlippageTolerance,
        ttl: 3600,
        recipient: walletAddress,
        feeOnTransfer: false,
      };
    }
    return {
      allowedSlippage: userSlippageTolerance,
      ttl: 3600,
      recipient: walletAddress,
      feeOnTransfer: isFeeOnTransfer,
    };
  }

  async createSwap(route: any, swapOptions: any, gasLimit: any, account: any) {
    const {
      methodName, // e.g. swapExactTokensForNATIVE,
      args, // e.g.[amountIn, amountOut, (pairBinSteps, versions, tokenPath) to, deadline]
      value, // e.g. 0x0
    } = Router.swapCallParameters(route, swapOptions);

    if (gasLimit) {
      const { request } = await this.publicClient.simulateContract({
        address: this.router,
        abi: routerABI,
        functionName: methodName,
        args: args,
        value: BigInt(value),
        gas: BigInt(gasLimit),
        account: account,
      });
      return request;
    } else {
      const { request } = await this.publicClient[chainId].simulateContract({
        address: this.router,
        abi: routerABI,
        functionName: methodName,
        args: args,
        value: BigInt(value),
        account: account,
      });
      return request;
    }
  }

  async executeSwap(walletClient, createSwap) {
    return await walletClient.writeContract(createSwap);
  }
}
